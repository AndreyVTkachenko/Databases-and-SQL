age - возраст.

Найдите средний возраст сотрудников по каждой должности из сущности staff.
Выведите только те должности, у которых средний возраст меньше 30 лет.
*/
SELECT post
FROM staff
GROUP BY post
HAVING AVG(age) < 30;


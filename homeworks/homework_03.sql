age - возраст.

Посчитайте и выведите количество сотрудников с должностью 'Рабочий' и возрастом не моложе 24 лет и не старше 49 лет.
*/
SELECT COUNT(*) AS count
FROM staff
WHERE post = 'Рабочий' AND age >= 24 AND age <= 49;


